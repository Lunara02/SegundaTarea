package tarea2;
/**
 * La enumeración tipoReunion define los tipos de reunión disponibles.
 * Puede ser utilizada para clasificar las reuniones según su propósito o tema.
 * Los valores posibles son TECNICA, MARKETING y OTRO.
 *
 * @author Cristobal Gonzalez
 */
public enum tipoReunion {
    /**
     * Indica que la reunión es de tipo Técnica.
     */
    TECNICA,
    /**
     * Indica que la reunión es de tipo Marketing.
     */
    MARKETING,
    /**
     * Indica que la reunión es de tipo Otro.
     */
    OTRO
}
